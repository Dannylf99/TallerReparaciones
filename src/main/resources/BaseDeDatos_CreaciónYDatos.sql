DROP DATABASE IF EXISTS tallerReparaciones;

CREATE DATABASE IF NOT EXISTS tallerReparaciones;
USE tallerReparaciones;

CREATE TABLE cliente(
id_cliente INT AUTO_INCREMENT PRIMARY KEY,
nombre varchar(45) NOT NULL,
dni varchar(10) NOT NULL UNIQUE,
telefono INT,
email VARCHAR(45) NOT NULL
);

CREATE TABLE usuario(
id_usuario int AUTO_INCREMENT PRIMARY KEY,
nombre_usuario varchar(45) NOT NULL,
dni varchar(10) NOT NULL UNIQUE,
password VARCHAR(45) NOT NULL,
rol ENUM('INVITADO', 'MECANICO', 'ADMINISTRADOR') NOT NULL DEFAULT 'INVITADO'
);

CREATE TABLE vehiculo(
id_vehiculo int AUTO_INCREMENT PRIMARY KEY,
matricula VARCHAR(10) NOT NULL UNIQUE,
marca VARCHAR(40),
modelo VARCHAR(40),
cliente_id int,
FOREIGN KEY (cliente_id) REFERENCES cliente(id_cliente)
	ON DELETE SET NULL
    ON UPDATE CASCADE
);

CREATE TABLE reparacion(
id_reparacion int AUTO_INCREMENT PRIMARY KEY,
descripcion VARCHAR(120),
fecha_entrada DATE,
coste_estimado DOUBLE,
estado ENUM('EN PREPARACIÓN','EN REPARACIÓN', 'FINALIZADO') DEFAULT 'EN PREPARACIÓN',
vehiculo_id int,
usuario_id int,
FOREIGN KEY (vehiculo_id) REFERENCES vehiculo(id_vehiculo)
	ON DELETE CASCADE
    ON UPDATE CASCADE,
FOREIGN KEY (usuario_id) REFERENCES usuario(id_usuario)
	ON DELETE SET NULL
    ON UPDATE CASCADE
);

